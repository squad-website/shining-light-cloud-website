// ================= HELPER =================

// cari member by ID (dipakai di member-card.js)
export function getMemberById(id) {
    return members.find(m => m.id == id);
}

// sort member (dipakai ranking & list)
export function getSortedMembers() {
    return [...members].sort((a, b) => b.point - a.point);
}

// tier logic (biar gak duplikat di banyak file)
export function getTier(point) {
    if (point >= 3500) return "S+";
    if (point >= 2000) return "S";
    if (point >= 1200) return "A+";
    if (point >= 700) return "A";
    if (point >= 400) return "B+";
    if (point >= 200) return "B";
    if (point >= 50) return "C";
    return "D";
}